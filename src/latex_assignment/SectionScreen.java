    private void AddSectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddSectionActionPerformed
        if (SectionName.getText().equals("")) {
            JOptionPane.showMessageDialog(f, "Missing Value, Enter Section Name", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            l.StartSection(SectionName.getText());
            JOptionPane.showMessageDialog(f, "Section Started");
            l.SetSection(1);
            document.setText(l.GetLatex());
            SectionName.setText("");
            SubSectionName.setText("");
        }
    }//GEN-LAST:event_AddSectionActionPerformed

    private void AddSubSectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddSubSectionActionPerformed
        // TODO add your handling code here:
        if (SubSectionName.getText().equals("")) {
            JOptionPane.showMessageDialog(f, "Missing Value, Enter Sub Section Name", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else if (l.GetSection() == 0) {
            JOptionPane.showMessageDialog(f, "Cannot Add Sub Section before a Section", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            l.SetSection(2);
            l.StartSubSection(SubSectionName.getText());
            JOptionPane.showMessageDialog(f, "Sub Section Started");
            document.setText(l.GetLatex());
            SectionName.setText("");
            SubSectionName.setText("");
        }
    }//GEN-LAST:event_AddSubSectionActionPerformed

