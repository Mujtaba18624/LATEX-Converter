    private void FillCurrentSection() {
        document.setText(l.GetLatex());
        if (l.GetSection() == 0) {
            currentSection.setText("No Section");
        } else if (l.GetSection() == 1) {
            currentSection.setText("Section");
        } else {
            currentSection.setText("Sub Section");
        }
    }

